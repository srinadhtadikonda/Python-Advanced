from turtle import *
title("Circle Drawing")			#used to set window title
setup(500, 500)				#used to set window width,height
bgcolor("red")					#used to set window background color
color("green")					#used to set shape color
pencolor("blue")				#used to set line color
pensize(5)					#used to set line width
shape("turtle")					#used to set shape
circle(100,360)				#used to draw circle
done()
